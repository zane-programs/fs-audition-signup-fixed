async function _fetchSug<T = any>(
  input: string,
  body: Object = {}
): Promise<T> {
  const req = await fetch("/api/sug/" + URL_ID + "/" + input, {
    method: "POST",
    headers: { accept: "application/json", "content-type": "application/json" },
    body: JSON.stringify(body),
  });
  return (await req.json()) as T;
}

